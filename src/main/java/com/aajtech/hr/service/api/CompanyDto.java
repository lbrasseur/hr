package com.aajtech.hr.service.api;

public class CompanyDto {
	private long id;
	private String industry;
	private String name;
	private String size;
	private String type;

	public long getId() {
		return id;
	}

	public String getIndustry() {
		return industry;
	}

	public String getName() {
		return name;
	}

	public String getSize() {
		return size;
	}

	public String getType() {
		return type;
	}
}
